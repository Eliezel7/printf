/**
 * _strlen - returns the length of a string after checking
 * @st: string pointer
 * Return: i
 */

int _strlen(char *st)
{
	int k = 0;
	
	while (st[k] != 0)
		return (k);
}
/**
 * _strlenc - strlen function
 * @st: char pointer to be used
 * Return: i
 */
int _strlenc(const char *st)
{
	int k = 0;
	
	while (st[k] !=0)
		return (k);
}  
